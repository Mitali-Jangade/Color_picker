let changeColorEl = document.getElementById("changeColor");
let colorEl = document.getElementById("color");

function whiteColor()
{
   
    changeColorEl.style.backgroundColor = "lightgrey";
    colorEl.textContent = "#e0e0e0";

}

function greenColor()
{
    changeColorEl.style.backgroundColor = "lightgreen";
    colorEl.textContent = "#06fcf97";
 
    
}

function blueColor()
{
    changeColorEl.style.backgroundColor = "lightblue";
    colorEl.textContent = "#56ccf2";

    
}

function purpleColor()
{
    changeColorEl.style.backgroundColor = "purple";
    colorEl.textContent = "#bb6bd9";

    
}